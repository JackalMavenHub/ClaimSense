-- Index for performance
CREATE INDEX IF NOT EXISTS idx_patent_sessions_user_id ON patent_sessions(user_id);
CREATE INDEX IF NOT EXISTS idx_session_questions_session_id ON session_questions(session_id);
CREATE INDEX IF NOT EXISTS idx_patent_drafts_session_id ON patent_drafts(session_id);
